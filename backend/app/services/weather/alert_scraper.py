_DINGZHI_URL = "https://d1.weather.com.cn/dingzhi/{code}.html"


class AlertScraper:
    """中国天气网预警爬虫。"""

    def fetch_alerts(self, city_name: str) -> list[WeatherAlert]:
        """获取指定城市的官方气象预警。

        Args:
            city_name: 城市中文名（如"苏州"）。

        Returns:
            WeatherAlert 列表，无预警或出错时返回空列表。
        """
        code = _CITY_CODE_MAP.get(city_name)
        if not code:
            logger.warning("预警爬虫：未知城市 %s", city_name)
            return []

        url = _DINGZHI_URL.format(code=code)
        try:
            resp = httpx.get(
                url,
                headers={
                    "Referer": "https://m.weather.com.cn/",
                    "User-Agent": (
                        "Mozilla/5.0 (Windows NT 10.0; Win64; x64) "
                        "AppleWebKit/537.36 (KHTML, like Gecko) "
                        "Chrome/120.0.0.0 Safari/537.36"
                    ),
                },
                timeout=10,
            )
            resp.raise_for_status()
        except httpx.HTTPError as exc:
            logger.warning("预警爬虫请求失败: %s", exc)
            return []

        return self._parse_alerts(resp.text)

    def _parse_alerts(self, html_text: str) -> list[WeatherAlert]:
        """解析 JS 变量 alarmDZ 中的预警数据。

        Args:
            html_text: 接口返回的 HTML/JS 文本。

        Returns:
            WeatherAlert 列表。
        """
        alerts: list[WeatherAlert] = []

        match = re.search(
            r"var\s+alarmDZ\s*=\s*({.*?});",
            html_text,
            re.DOTALL,
        )
        if not match:
            return alerts

        try:
            json_str = match.group(1)
            json_str = json_str.replace("'", '"')
            data = json.loads(json_str)
        except json.JSONDecodeError:
            return self._parse_alerts_regex(html_text)

        warnings = data.get("w", [])
        for w in warnings:
            alerts.append(
                WeatherAlert(
                    title=w.get("w5", ""),
                    description=w.get("w7", ""),
                    severity=w.get("w8", "blue").lower(),
                )
            )

        return alerts

    def _parse_alerts_regex(self, html_text: str) -> list[WeatherAlert]:
        """JSON 解析失败时的正则备用解析。

        Args:
            html_text: 接口返回的 HTML/JS 文本。

        Returns:
            WeatherAlert 列表。
        """
        alerts: list[WeatherAlert] = []
        pattern = re.compile(
            r"w5\s*[:=]\s*['\"](.*?)['\"]\s*,\s*"
            r"w7\s*[:=]\s*['\"](.*?)['\"]\s*,\s*"
            r"w8\s*[:=]\s*['\"](.*?)['\"]",
            re.DOTALL,
        )
        for m in pattern.finditer(html_text):
            alerts.append(
                WeatherAlert(
                    title=m.group(1).strip(),
                    description=m.group(2).strip(),
                    severity=m.group(3).strip().lower(),
                )
            )
        return alerts
